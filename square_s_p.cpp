//Да се напише програма, която въвежда от клавиатурата страната a на квадрат и изчислява и извежда лицето и обиколката му.

#include<iostream>
using namespace std;
int main()
{
int a; //страната на квадрата
int s,p; //лице и обиколка
cout << "a = ";
cin >> a; //въвеждаме a
p = 4 * a; //пресмятаме обиколката
s = a * a; //пресмятаме периметъра
cout << "The perimeter is " << p << endl; //извеждаме обиколката на екран
cout << "The s is " << s << endl; //извеждаме лицето на екран
return 0;
}
